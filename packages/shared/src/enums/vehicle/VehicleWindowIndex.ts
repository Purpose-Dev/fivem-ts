/**
 * Enum representing the index of different windows in a vehicle.
 *
 * @enum{number}
 */
export enum VehicleWindowIndex {
	/** representing the front left window of the vehicle. */
	FrontLeftWindow = 0,
	/** representing the front right window of the vehicle. */
	FrontRightWindow = 1,
	/** representing the back left window of the vehicle. */
	BackLeftWindow = 2,
	/** representing the back right window of the vehicle. */
	BackRightWindow = 3,
	/** representing an extra window, usually custom, additional or Middle Left Window. */
	ExtraWindow1 = 4,
	/** representing an extra window, usually custom, additional or Middle Rear Window. */
	ExtraWindow2 = 5,
	/** representing an extra window, usually custom, additional or WindScreen. */
	ExtraWindow3 = 6,
	/** representing an extra window, usually custom, additional or Rear WindScreen. */
	ExtraWindow4 = 7,
}
