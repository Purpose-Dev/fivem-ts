/**
 * Enumeration representing different types of vehicle window tints.
 *
 * @enum {number}
 */
export enum VehicleWindowTint {
    /** No Tint */
    None = 0,
    /** Completely black tint */
    PureBlack = 1,
    /** Dark smoke tint */
    DarkSmoke = 2,
    /** Light smoke tint */
    LightSmoke = 3,
    /** Factory-provided tint */
    Stock = 4,
    /** Limo-style dark tint */
    Limo = 5,
    /** Green tint */
    Green = 6,
}
