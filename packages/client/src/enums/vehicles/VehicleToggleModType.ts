/**
 * Enum representing different types of toggleable vehicle modifications.
 *
 * Values:
 * - Turbo: Represents the turbo modification (value 18).
 * - TireSmoke: Represents the tire smoke modification (value 20).
 * - XenonHeadlights: Represents the xenon headlights modification (value 22).
 *
 * @enum{number}
 */
export enum VehicleToggleModType {
	Turbo = 18,
	TireSmoke = 20,
	XenonHeadlights = 22,
}
