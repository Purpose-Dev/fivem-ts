/**
 * Enumeration representing the different neon light positions on a vehicle.
 *
 * Values:
 * - `Left`: Represents the neon light located on the left side of the vehicle.
 * - `Right`: Represents the neon light located on the right side of the vehicle.
 * - `Front`: Represents the neon light located on the front of the vehicle.
 * - `Back`: Represents the neon light located on the back of the vehicle.
 *
 * @enum{number}
 */
export enum VehicleNeonLight {
    Left,
    Right,
    Front,
    Back,
}
