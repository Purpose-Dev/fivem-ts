/**
 * Enum representing the possible states of a vehicle's roof.
 *
 * Values:
 * - `Closed`: Indicates the roof is fully closed.
 * - `Closing`: Indicates the roof is in the process of closing.
 * - `Opened`: Indicates the roof is fully open.
 * - `Opening`: Indicates the roof is in the process of opening.
 *
 * @enum{number}
 */
export enum VehicleRoofState {
    Closed,
    Closing,
    Opened,
    Opening,
}
